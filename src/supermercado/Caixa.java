    private boolean addProduto() {
        Double quantidade;
        Produto produto;
        ItemVenda itemVenda;
        if (txtCodigoProduto.getText().isEmpty()) {
            return true;
        }
        produto = new ProdutoController(EntityManagerHelper.entityManagerFactory()).bySimpleNaturalId(txtCodigoProduto.getText().trim());
        if (produto == null) {
            JOptionPane.showMessageDialog(rootPane, "Produto não encontrado");
            return true;
        }
        String retorno = JOptionPane.showInputDialog(rootPane, "Insira a quantidade em ("+produto.getUnidade().toString()+")", produto.getDescricao(), JOptionPane.PLAIN_MESSAGE);
        if(retorno == null || retorno.isEmpty()) {
            return true;
        }
        quantidade = Double.valueOf(retorno.replace(',', '.'));
        if (quantidade == null) {
            return true;
        }
        txtCodigoProduto.setText("");
        itemVenda = new ItemVenda();
        itemVenda.setValor(produto.getValorUnitario().multiply(new BigDecimal(quantidade)));
        itemVenda.setQuantidade(quantidade);
        itemVenda.setProduto(produto);
        listaVenda.add(itemVenda);
        model.addElement(itemVenda.toString());
        atualizarTotal();
        return false;
    }

