    private void atualizarTotal() {
        BigDecimal total = new BigDecimal(0);
        for (ItemVenda itemVenda : listaVenda) {
            total = total.add(itemVenda.getValor());
        }
        venda.setValor(total);
        txtTotal.setText(MathUtil.Currency.format(total));
    }

    private void initNewVenda() {
        listaVenda.clear();
        venda = new Venda();
        venda.setCaixa(caixa);
        venda.setItensVenda(listaVenda);
        model.clear();
        txtTotal.setText("");
    }
